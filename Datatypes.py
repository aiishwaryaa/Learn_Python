# datatypes  in python 
# 1. Integers
# 2. Floats
# 3. Complex Numbers
# 4. Strings
# 5. Boolean
# 6. List
# 7. Tuple                             
# 8. Dictionary
# 9. Set
# 10. NoneType
 
x= int(20)
print(x)

y= float(20.5)
print(y)

z= complex(3,4)
print(z)

a= str("Hello")
print(a)

b= bool(1)
print(b)

c= list([1,2,3,4,5])
print(c)

d= tuple((1,2,3,4,5))
print(d)

e= dict({"name":"John","age":30})
print(e)

f= set([1,2,3,4,5])
print(f)

g= None
print(g)  # NoneType
